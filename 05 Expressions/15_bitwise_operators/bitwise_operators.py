print(1 << 3)   # SHIFT LEFT:   00000001            ==> 00001000 (8)
print(32 >> 3)  # SHIFT RIGHT:  00010000            ==> 00000010 (4)
print(64 & 63)  # AND:          01000000 & 00111111 ==> 00000000 (0) 
print(64 | 63)  # OR:           01000000 | 00111111 ==> 01111111 (127) 
print(60 ^ 13)  # XOR:          00111100 ^ 00001101 ==> 00110001 (49)
